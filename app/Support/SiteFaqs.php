    /**
     * 扁平化所有題目，供 FAQPage JSON-LD 使用
     */
    public static function flat(): array
    {
        $flat = [];
        foreach (self::all() as $category) {
            foreach ($category['items'] as $item) {
                $flat[] = $item;
            }
        }

        return $flat;
    }

    /**
     * 取單一分類
     */
    public static function category(string $key): array
    {
        return self::all()[$key] ?? [];
    }
